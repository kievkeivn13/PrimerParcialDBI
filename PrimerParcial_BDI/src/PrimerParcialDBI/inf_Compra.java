    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        // TODO add your handling code here:
        
        
        try {
            Connection cn = DriverManager.getConnection(mdi_Principal.BD, mdi_Principal.Usuario, mdi_Principal.Contraseña);
            
            //Guardando en el detalle de la venta
            PreparedStatement pst1 = cn.prepareStatement("insert into Compra_d values(?,?,?,?)");

            //
            pst1.setString(1, txtId_Compra.getText().trim());
            pst1.setString(2, txtCantidad.getText().trim());
            pst1.setString(3, txtId_Producto.getText().trim());
            pst1.setString(4, txtPrecio.getText().trim());

            pst1.executeUpdate();           
       
            //Llenando la tabla
            
             this.tabla.setModel(model); 
            String titulo[] = {"Id_Compra", "Id_Producto", "Cantidad", "Precio"};
            model.setColumnIdentifiers(titulo);
            
            String registros[] = new String[100];
            
            registros[0] = txtId_Compra.getText();
            registros[1] = txtId_Producto.getText();
            registros[2] = txtCantidad.getText();
            registros[3] = txtPrecio.getText();
            model.addRow(registros);

            JOptionPane.showMessageDialog(this, "¡REGISTRO EXITOSO!", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            txtCantidad.setText("");
            txtPrecio.setText("");
            txtId_Producto.setText("");
            sumaColumnaPrecio();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error en registro", "Warning", JOptionPane.WARNING_MESSAGE);
            System.out.println(e);
        }
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

